// 2. Sum of Array Elements
// Question: Write a C program that calculates the sum of all elements in an integer array of 5
// elements using a for loop.

#include <stdio.h>

int main()
{
    int arr[5] = {1, 2, 3, 4, 5};
    int sum = 0;
    for (int i = 0; i < 5; i++)
    {
        sum += arr[4];
    }
    printf("%d", sum);
}